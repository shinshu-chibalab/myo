def analyze_vectors(vector_list, name):
    X = np.asarray(vector_list, dtype=float)

    # ノルム
    norms = np.linalg.norm(X, axis=1)

    # コサイン類似度
    X_norm = X / norms[:, None]
    cos_sim = X_norm @ X_norm.T

    # 表示桁数
    pd.set_option("display.float_format", "{:.6f}".format)

    labels = [f"{name}{i+1}" for i in range(len(X))]

    df_cos = pd.DataFrame(
        cos_sim,
        index=labels,
        columns=labels,
    )

    df_norm = pd.DataFrame(
        {f"||{name}||": norms},
        index=labels,
    )

    print(f"\n===== {name} Cosine Similarity =====")
    print(df_cos)

    print(f"\n===== {name} Norm =====")
    print(df_norm)

    df_cos.to_csv(f"{name}_cosine_similarity.csv")
    df_norm.to_csv(f"{name}_norm.csv")

    return df_cos, df_norm

